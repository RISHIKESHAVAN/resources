## Sometimes in cases where normal distribution does not apply to the data,
## histograms and qq plots would serve as a relatively succinct summary.
## When you need to describe such data to someone who is used to receiving just 
## two numbers, the average and the standard deviation. In this case, that's not 
## an appropriate summary, but they still want something compact.

## Here, John Tukey offers some advice. Provide a five number summary composed of 
## the range along with the quartiles, 25th, 50th, and 75th percentile.
## Tukey further suggests that we ignore outliers when computing the range,
## and instead plot these as independent points. Finally, he suggests that we plot 
## this five number summary as a box with whiskers.