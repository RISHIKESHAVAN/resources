## Data Visualization
## Key points:
## Plots of data easily communicate information that is difficult to extract 
## from tables of raw values.
## Data visualization is a key component of exploratory data analysis (EDA), 
## in which the properties of data are explored through visualization and 
## summarization techniques.
## Data visualization can help discover biases, systematic errors, mistakes and 
## other unexpected problems in data before those data are incorporated into 
## potentially flawed analysis.
## This course covers the basics of data visualization and EDA in R using 
## the ggplot2 package and motivating examples from world health, economics and 
## infectious disease.

