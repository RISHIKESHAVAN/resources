## The percentiles are the quantiles you obtain when you define p as 0.01, 0.02
## up to 0.99, 1%, 2%, 3%, etc. This is why we call them percentiles.
## We call, for example, the case of p equals 0.25 the 25th percentiles.
## This gives us the number for which 25% other data is below.
## The most famous percentile is the 50th, also known as the median.
## Note that for the normal distribution, the median and the average
## are the same. But this is not generally the case. Another special case 
## that receives a name are the quartiles, which are obtained when we 
## set p to be 0.25, 0.50, and 0.75.