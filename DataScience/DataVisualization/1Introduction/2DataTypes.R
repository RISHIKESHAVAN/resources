## Know what type of data it is.
## 2 types of variables: Categoricals and Numericals.
## Each is further divided into two groups.
## Categoricals: Variables that are defined by a small number of groups
## > Ordinals: Can be ordered. eg, spiciness
## > Non-ordinals: Can not be ordered.
## Numericals: Variables not defined by a small number of groups. 
## eg, population sizes, murder rates and heights.
## > Discrete: Counts such as population sizes are discrete because they 
## have to be round numbers. Note that discrete numeric data can be considered ordinal.
## > Continuous: those that can take any value such as heights if measured 
## with enough precision. eg, 68.12 & 68.11 inches in heights.

## Ordinal data: variables belonging to a small number of different groups
## with each group having many members.
## Discrete numerical variables: when we have many groups with few cases in each group
## So for example, the number of packs of cigarettes a person smokes a day
## rounded to the closest pack-- so 0, 1, or 2-- would be considered ordinal.
## While the number of cigarettes that we smoke-- 0, 1, 2, 3 up to maybe 36--
## would be considered a numerical variable.