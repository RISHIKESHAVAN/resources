## Hans Rosling was the co-founder of the Gapminder Foundation, an organization
## dedicated to educating the public by using data to dispel common myths about 
## the so-called developing world. The organization uses data to show how actual 
## trends in health and economics contradict the narratives that emanate from 
## sensationalist media coverage of catastrophes, tragedies, and other 
## unfortunate events. As stated in the Gapminder Foundation's website,
## "Journalists and lobbyists tell dramatic stories. That's their job.
## They tell stories about extraordinary events and unusual people.
## The piles of dramatic stories pile up in people's minds into an overdramatic 
## worldview and strong negative stress feelings. The world is getting worse.
## It's we versus them. Other people are strange. The population just keeps growing.
## And nobody cares."
## Hans Rosling conveyed actual data-based trends, in a dramatic way of his own,
## using effective data visualization. This section is based on these talks that
## exemplify this approach to education.
## "New Insights on Poverty" and "The Best Stats You've Ever Seen"
